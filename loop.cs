using System;



public class Count10
{
    static void Main(string[] args)
    {
        int a;
        int b;
        int c;
        a = 0;
        b = 0;
        c = 0;

        while (a <= 10)
        {
            Console.WriteLine(a);
            a++;
        }
        while (b <= 10)
        {
            Console.WriteLine(b);
            b++;
        }
        while (c <= 10)
        {
            Console.WriteLine(c);
            c++;
        }
    }
         
}
